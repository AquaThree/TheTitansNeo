	public void animate(EntitySpiderTitan entity, float limbSwing, float limbSwingAmount, float ageInTicks, float headYaw, float headPitch) {
		float partialTicks = Minecraft.getInstance().getTimer().getGameTimeDeltaPartialTick(false);
		this.animator.update(entity);
		this.setAngles();
		if (entity.deathTicks <= 0) {
			if (this.riding) {
				this.frontrightupperarm.yRot = 0.87266463F;
				this.frontleftupperarm.yRot = -0.87266463F;
				this.frontrightupperleg.yRot = 1.3089969F;
				this.frontleftupperleg.yRot = -1.3089969F;
				this.backrightupperleg1.yRot = 1.8325957F;
				this.backleftupperleg1.yRot = -1.8325957F;
				this.backrightupperleg2.yRot = 2.1816616F;
				this.backleftupperleg2.yRot = -2.1816616F;
				this.frontrightupperarm.xRot = -0.8F;
				this.frontleftupperarm.xRot = -0.8F;
				this.frontrightupperleg.xRot = -0.8F;
				this.frontleftupperleg.xRot = -0.8F;
				this.backrightupperleg1.xRot = -0.8F;
				this.backleftupperleg1.xRot = -0.8F;
				this.backrightupperleg2.xRot = -0.8F;
				this.backleftupperleg2.xRot = -0.8F;
			} else if (entity.getAnimationID() == 0) {
				this.frontrightupperarm.yRot = 0.87266463F + Mth.cos(limbSwing * 0.5F + 2.0F) * 0.3F * limbSwingAmount;
				this.frontleftupperarm.yRot = -0.87266463F + Mth.cos(limbSwing * 0.5F + 4.0F) * 0.3F * limbSwingAmount;
				this.frontrightupperleg.yRot = 1.3089969F + Mth.cos(limbSwing * 0.5F + 6.0F) * 0.3F * limbSwingAmount;
				this.frontleftupperleg.yRot = -1.3089969F + Mth.cos(limbSwing * 0.5F) * 0.3F * limbSwingAmount;
				this.backrightupperleg1.yRot = 1.8325957F + Mth.cos(limbSwing * 0.5F + 6.0F) * 0.3F * limbSwingAmount;
				this.backleftupperleg1.yRot = -1.8325957F + Mth.cos(limbSwing * 0.5F + 4.0F) * 0.3F * limbSwingAmount;
				this.backrightupperleg2.yRot = 2.1816616F + Mth.cos(limbSwing * 0.5F + 2.0F) * 0.3F * limbSwingAmount;
				this.backleftupperleg2.yRot = -2.1816616F + Mth.cos(limbSwing * 0.5F) * 0.3F * limbSwingAmount;
				this.frontrightupperarm.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 2.0F) * 0.15F * limbSwingAmount;
				this.frontleftupperarm.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 4.0F) * 0.15F * limbSwingAmount;
				this.frontrightupperleg.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 6.0F) * 0.15F * limbSwingAmount;
				this.frontleftupperleg.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F) * 0.15F * limbSwingAmount;
				this.backrightupperleg1.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 6.0F) * 0.15F * limbSwingAmount;
				this.backleftupperleg1.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 4.0F) * 0.15F * limbSwingAmount;
				this.backrightupperleg2.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F + 2.0F) * 0.15F * limbSwingAmount;
				this.backleftupperleg2.xRot = -1.2217305F + Mth.cos(limbSwing * 0.5F) * 0.15F * limbSwingAmount;
			}
			if (!entity.onGround() && !this.riding) {
				this.abdoman.xRot -= Math.cos(limbSwing * 0.35F - 1.5F) * 0.1F * limbSwingAmount - 0.25F + entity.walkAnimation.speed(partialTicks);
				this.frontrightupperarm.yRot = 0.5F;
				this.frontleftupperarm.yRot = -0.5F;
				this.frontrightupperleg.yRot = 1.4F + entity.walkAnimation.speed(partialTicks);
				this.frontleftupperleg.yRot = -1.4F - entity.walkAnimation.speed(partialTicks);
				this.backrightupperleg1.yRot = 1.8F + entity.walkAnimation.speed(partialTicks);
				this.backleftupperleg1.yRot = -1.8F - entity.walkAnimation.speed(partialTicks);
				this.backrightupperleg2.yRot = 2.0F + entity.walkAnimation.speed(partialTicks);
				this.backleftupperleg2.yRot = -2.0F - entity.walkAnimation.speed(partialTicks);
				this.frontrightupperleg.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F) * 0.25F;
				this.frontleftupperleg.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F - 0.5F) * 0.25F;
				this.backrightupperleg1.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F - 1.0F) * 0.25F;
				this.backleftupperleg1.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F - 1.5F) * 0.25F;
				this.backrightupperleg2.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F - 2.0F) * 0.25F;
				this.backleftupperleg2.xRot = -1.2217305F + Mth.cos(ageInTicks * 0.2F - 2.5F) * 0.25F;
				if (entity.getAnimationID() == 0) {
					this.frontrightupperarm.xRot = -2.0F;
					this.frontleftupperarm.xRot = -2.0F;
				}
			}
			if (entity.getAnimationID() == 0) {
				this.head.xRot = (0.01F + 0.01F * Mth.cos(ageInTicks * 0.1F)) * Mth.PI;
				this.abdoman.xRot = -((0.01F + 0.01F * Mth.cos(ageInTicks * 0.1F))) * Mth.PI;
				this.thorax.y = 15.0F - 0.025F * Mth.cos(ageInTicks * 0.1F) * Mth.PI;
			}
			if (entity.getAnimationID() == 6 && entity.getAnimationTick() >= 30 && entity.getAnimationTick() < 50) {
				this.abdoman.xRot = 0.1F * Mth.cos(ageInTicks * 1.0F) * Mth.PI;
			}
			if (entity.getAnimationID() == 6 && entity.getAnimationTick() >= 50 && entity.getAnimationTick() < 100) {
				this.abdoman.xRot = 0.1F * Mth.cos(ageInTicks * 1.5F) * Mth.PI;
			}
			float faceYaw = headYaw * Mth.PI / 180.0F;
			float facePitch = headPitch * Mth.PI / 180.0F;
			this.head.xRot += facePitch * 0.9F;
			this.head.yRot += faceYaw * 0.9F;
			if (entity.getAnimationID() == 1)
				switch (entity.getAntiTitanAttackAnimationID()) {
				case 0:
					this.animateAntiTitanAttack1();
					break;
				case 1:
					this.animateAntiTitanAttack2();
					break;
				case 2:
					this.animateAntiTitanAttack3();
					break;
				case 3:
					this.animateAntiTitanAttack4();
					break;
				}
			this.animateBirth();
			this.animateBite();
			this.animateSpit();
			this.animateStomp();
			this.animateSwat();
			this.animateShootLightning();
			this.animateShootWeb();
			this.animateStunned();
			this.animateCrush();
		} else {
			this.animateDeath();
		}
	}

