	public void animate(EntityPigZombieTitan entity, float limbSwing, float limbSwingAmount, float ageInTicks, float headYaw, float headPitch) {
		float partialTicks = Minecraft.getInstance().getTimer().getGameTimeDeltaPartialTick(false);
		if (!entity.onGround()) {
			limbSwingAmount = 0.0F;
		}
		this.animator.update(entity);
		this.setAngles();
		float fo = 0.16655F;

		if (entity.deathTicks <= 0) {
			float f6 = Mth.cos(ageInTicks * 0.1F);
			if (entity.getAnimationID() == 0) {
				this.middleBody.xRot = (0.001F + 0.005F * f6) * Mth.PI;
				this.topBody.xRot = (0.001F + 0.005F * f6) * Mth.PI;
				this.head.xRot = (0.001F + 0.0075F * f6) * Mth.PI;
				this.rightShoulder.yRot = (-0.001F + 0.01F * f6) * Mth.PI;
				this.leftShoulder.yRot = (0.001F + -0.01F * f6) * Mth.PI;
			}
			if (this.riding) {
				this.rightThigh.xRot = -1.5707964F;
				this.leftThigh.xRot = -1.5707964F;
				this.rightThigh.yRot = 0.31415927F;
				this.leftThigh.yRot = -0.31415927F;
				this.rightCalf.xRot = 1.0471976F;
				this.leftCalf.xRot = 1.0471976F;
			} else if (entity.getAnimationID() == 0) {
				this.torso.zRot = Mth.cos(limbSwing * fo) * 0.2F * limbSwingAmount;
				this.middleBody.zRot = Mth.cos(limbSwing * fo - 1.0F) * 0.2F * limbSwingAmount;
				this.topBody.zRot = Mth.cos(limbSwing * fo - 2.0F) * 0.2F * limbSwingAmount;
				this.rightThigh.xRot = Mth.cos(limbSwing * fo - 0.5F) * 0.75F * limbSwingAmount;
				this.leftThigh.xRot = Mth.cos(limbSwing * fo + 2.6415927F) * 0.75F * limbSwingAmount;
				this.rightCalf.xRot = Mth.cos(limbSwing * fo + 3.1415927F) * 0.75F * limbSwingAmount;
				this.leftCalf.xRot = Mth.cos(limbSwing * fo) * 0.75F * limbSwingAmount;
				this.torso.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				this.rightThigh.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				this.leftThigh.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				if (this.rightCalf.xRot < 0.0F) {
					this.rightCalf.xRot = 0.0F;
				}
				if (this.leftCalf.xRot < 0.0F) {
					this.leftCalf.xRot = 0.0F;
				}
			}
			float faceYaw = headYaw * Mth.PI / 180.0F;
			float facePitch = headPitch * Mth.PI / 180.0F;
			if (entity.getAnimationID() == 0) {
				this.head.xRot += facePitch * 0.3F;
				this.head.yRot += faceYaw * 0.3F;
				this.topBody.xRot += facePitch * 0.3F;
				this.topBody.yRot += faceYaw * 0.3F;
				this.middleBody.xRot += facePitch * 0.3F;
				this.middleBody.yRot += faceYaw * 0.3F;
			} else {
				this.head.xRot += facePitch * 0.9F;
				this.head.yRot += faceYaw * 0.9F;
			}
			if (!entity.onGround() && !this.riding && entity.getY() > -63.0D) {
				this.torso.zRot = 0.0F;
				this.middleBody.zRot = 0.0F;
				this.topBody.zRot = 0.0F;
				this.head.zRot = 0.0F;
				this.rightShoulder.xRot -= entity.walkAnimation.speed(partialTicks);
				this.leftShoulder.xRot -= entity.walkAnimation.speed(partialTicks);
				this.head.xRot -= entity.walkAnimation.speed(partialTicks);
				this.torso.xRot += entity.walkAnimation.speed(partialTicks);
				this.rightThigh.zRot = 0.25F;
				this.leftThigh.zRot = -0.25F;
				this.rightThigh.yRot = -0.25F;
				this.leftThigh.yRot = 0.25F;
				this.rightThigh.xRot = Mth.cos(ageInTicks * 0.2F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
				this.leftThigh.xRot = Mth.cos(ageInTicks * 0.2F - Mth.PI) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
				this.rightCalf.xRot = 0.5F - Mth.cos(ageInTicks * 0.2F + 0.5F) * 0.5F;
				this.leftCalf.xRot = 0.5F - Mth.cos(ageInTicks * 0.2F - 2.6415927F) * 0.5F;
			}
			if (entity.getAnimationID() == 11 && entity.getAnimationTick() > 30 && entity.getAnimationTick() < 160) {
				this.head.zRot = Mth.cos(ageInTicks * 0.25F - 3.0F) * 0.5F;
				this.topBody.zRot = Mth.cos(ageInTicks * 0.25F - 2.0F) * 0.25F;
				this.middleBody.zRot = Mth.cos(ageInTicks * 0.25F - 1.0F) * 0.25F;
				this.torso.zRot = Mth.cos(ageInTicks * 0.25F) * 0.25F;
			}
			if (entity.getAnimationID() == 12 && entity.getAnimationTick() > 30 && entity.getAnimationTick() < 50) {
				this.rightForearm.xRot = 0.1F * Mth.cos(ageInTicks * 2.0F) * Mth.PI;
				this.leftForearm.xRot = -(0.1F * Mth.cos(ageInTicks * 2.0F) * Mth.PI);
			}
			if (entity.getAnimationID() == 1) {
				switch (entity.getAntiTitanAttackAnimationID()) {
				case 0:
					this.animateAntiTitanAttack1();
					break;
				case 1:
					this.animateAntiTitanAttack2();
					break;
				case 2:
					this.animateAntiTitanAttack3();
					break;
				case 3:
					this.animateAntiTitanAttack4();
					break;
				}
			}
			this.animateBirth();
			this.animateRoar();
			this.animateRangedAttackThrow();
			this.animateSlam();
			this.animateSwat();
			this.animateSwordDig();
			this.animateLightning();
			this.animateStomp();
			this.animateDownwardSlash();
			this.animateStun();
			this.animateSidewaySlash();
		} else {
			this.animateDeath();
		}
	}

