	public void animate(EntityCreeperTitan entity, float limbSwing, float limbSwingAmount, float ageInTicks, float headYaw, float headPitch) {
		float partialTicks = Minecraft.getInstance().getTimer().getGameTimeDeltaPartialTick(false);
		this.animator.update(entity);
		this.setAngles();
		float f6 = Mth.cos(ageInTicks * 0.1F);
		if (entity.deathTicks <= 0) {
			float faceYaw = headYaw * Mth.PI / 180.0F;
			float facePitch = headPitch * Mth.PI / 180.0F;
			this.bottomBody.zRot = Mth.cos(limbSwing * 0.25F) * 0.3F * limbSwingAmount;
			this.bottomMiddle.zRot = Mth.cos(limbSwing * 0.25F - 1.0F) * 0.3F * limbSwingAmount;
			this.bottomTop.zRot = Mth.cos(limbSwing * 0.25F - 2.0F) * 0.3F * limbSwingAmount;
			this.head.zRot = Mth.cos(limbSwing * 0.25F - 3.0F) * 0.3F * limbSwingAmount;
			if (entity.getAnimationID() == 8 && entity.getAnimationTick() > 130 && entity.getAnimationTick() < 430) {
				this.bottomMiddle.yRot = Mth.cos(ageInTicks * 0.05F + 2.0F) * 0.2F;
				this.bottomTop.yRot = Mth.cos(ageInTicks * 0.05F + 1.0F) * 0.2F;
				this.head.yRot = Mth.cos(ageInTicks * 0.05F) * 0.2F;
				this.bottomMiddle.xRot = (-0.025F + -0.0125F * Mth.cos(ageInTicks * 0.1F - 2.0F)) * Mth.PI;
				this.bottomTop.xRot = (0.0125F + 0.0125F * Mth.cos(ageInTicks * 0.1F - 1.0F)) * Mth.PI;
				this.head.xRot = (0.0125F + 0.0125F * f6) * Mth.PI;
			}
			if (entity.getAnimationID() == 13 && entity.getAnimationTick() < 5) {
				this.bottomMiddle.xRot = (-0.005F + -0.005F * Mth.cos(ageInTicks * 0.1F - 2.0F)) * Mth.PI;
				this.bottomTop.xRot = (0.005F + 0.005F * Mth.cos(ageInTicks * 0.1F - 1.0F)) * Mth.PI;
				this.head.xRot = (0.005F + 0.005F * f6) * Mth.PI;
			}
			if (this.riding) {
				this.rightbackupperleg.setPos(-3.0F, 21.0F, 1.0F);
				this.leftfrontupperleg.setPos(3.0F, 21.0F, -1.0F);
				this.bottomBody.setPos(0.0F, 21.0F, 0.0F);
				this.leftbackupperleg.setPos(3.0F, 21.0F, 1.0F);
				this.rightfrontupperleg.setPos(-3.0F, 21.0F, -1.0F);
				this.leftfrontupperleg.xRot = -1.6F;
				this.leftbackupperleg.xRot = -1.6F;
				this.rightfrontupperleg.xRot = -1.6F;
				this.rightbackupperleg.xRot = -1.6F;
				this.leftfrontupperleg.yRot = -0.725F;
				this.leftbackupperleg.yRot = -2.25F;
				this.rightfrontupperleg.yRot = 0.725F;
				this.rightbackupperleg.yRot = 2.25F;
				this.leftbackcalf.xRot = 1.0F;
				this.leftfrontcalf.xRot = 1.0F;
				this.rightbackcalf.xRot = 1.0F;
				this.rightfrontcalf.xRot = 1.0F;
			} else if (entity.getAnimationID() == 0) {
				this.bottomMiddle.xRot = (-0.025F + -0.0125F * Mth.cos(ageInTicks * 0.1F - 2.0F)) * Mth.PI;
				this.bottomTop.xRot = (0.0125F + 0.0125F * Mth.cos(ageInTicks * 0.1F - 1.0F)) * Mth.PI;
				this.head.xRot = (0.0125F + 0.0125F * f6) * Mth.PI;
				this.leftfrontupperleg.xRot = -1.3089969F + Mth.cos(limbSwing * 0.25F + 0.25F) * 0.75F * limbSwingAmount;
				this.leftbackupperleg.xRot = -1.3089969F + Mth.cos(limbSwing * 0.25F + 3.3915927F) * 0.75F * limbSwingAmount;
				this.rightfrontupperleg.xRot = -1.3089969F + Mth.cos(limbSwing * 0.25F + 3.3915927F) * 0.75F * limbSwingAmount;
				this.rightbackupperleg.xRot = -1.3089969F + Mth.cos(limbSwing * 0.25F + 0.25F) * 0.75F * limbSwingAmount;
				this.leftfrontupperleg.yRot = -0.525F + Mth.cos(limbSwing * 0.25F + 2.6415927F) * 0.5F * limbSwingAmount;
				this.leftbackupperleg.yRot = -2.625F + Mth.cos(limbSwing * 0.25F - 0.5F) * 0.5F * limbSwingAmount;
				this.rightfrontupperleg.yRot = 0.525F - Mth.cos(limbSwing * 0.25F - 0.75F) * 0.5F * limbSwingAmount;
				this.rightbackupperleg.yRot = 2.625F - Mth.cos(limbSwing * 0.25F + 2.6415927F) * 0.5F * limbSwingAmount;
				this.leftbackcalf.xRot = 0.7853982F + Mth.cos(limbSwing * 0.25F) * 0.5F * limbSwingAmount;
				this.leftfrontcalf.xRot = 0.7853982F + Mth.cos(limbSwing * 0.25F + Mth.PI) * 0.5F * limbSwingAmount;
				this.rightbackcalf.xRot = 0.7853982F + Mth.cos(limbSwing * 0.25F + Mth.PI) * 0.5F * limbSwingAmount;
				this.rightfrontcalf.xRot = 0.7853982F + Mth.cos(limbSwing * 0.25F) * 0.5F * limbSwingAmount;
				if (this.leftfrontupperleg.xRot > -1.3089969F) {
					this.leftfrontupperleg.xRot = -1.3089969F;
				}
				if (this.rightfrontupperleg.xRot > -1.3089969F) {
					this.rightfrontupperleg.xRot = -1.3089969F;
				}
				if (this.rightbackupperleg.xRot > -1.3089969F) {
					this.rightbackupperleg.xRot = -1.3089969F;
				}
				if (this.leftbackupperleg.xRot > -1.3089969F) {
					this.leftbackupperleg.xRot = -1.3089969F;
				}
				if (this.rightbackcalf.xRot < 0.7853982F) {
					this.rightbackcalf.xRot = 0.7853982F;
				}
				if (this.rightfrontcalf.xRot < 0.7853982F) {
					this.rightfrontcalf.xRot = 0.7853982F;
				}
				if (this.leftbackcalf.xRot < 0.7853982F) {
					this.leftbackcalf.xRot = 0.7853982F;
				}
				if (this.leftfrontcalf.xRot < 0.7853982F) {
					this.leftfrontcalf.xRot = 0.7853982F;
				}
				if (this.rightbackcalf.xRot < 0.7853982F) {
					this.rightbackcalf.xRot = 0.7853982F;
				}
				if (this.rightfrontcalf.xRot < 0.7853982F) {
					this.rightfrontcalf.xRot = 0.7853982F;
				}
			}
			this.head.xRot += facePitch * 0.3F;
			this.head.yRot += faceYaw * 0.3F;
			this.bottomTop.xRot += facePitch * 0.3F;
			this.bottomTop.yRot += faceYaw * 0.3F;
			this.bottomMiddle.xRot += facePitch * 0.3F;
			this.bottomMiddle.yRot += faceYaw * 0.3F;
			if (!entity.onGround() && !this.riding) {
				this.bottomBody.zRot = 0.0F;
				this.bottomMiddle.zRot = 0.0F;
				this.bottomTop.zRot = 0.0F;
				this.head.zRot = 0.0F;
				this.head.xRot -= entity.walkAnimation.speed(partialTicks);
				this.bottomBody.xRot += entity.walkAnimation.speed(partialTicks);
				this.leftfrontupperleg.yRot = -0.25F;
				this.rightbackupperleg.yRot = 2.75F;
				this.leftbackupperleg.yRot = -2.75F;
				this.rightfrontupperleg.yRot = 0.25F;
				this.rightbackupperleg.xRot = -2.0F - Mth.cos(ageInTicks * 0.2F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) - entity.walkAnimation.speed(partialTicks);
				this.leftbackupperleg.xRot = -2.0F - Mth.cos(ageInTicks * 0.2F - 3.1415927F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) - entity.walkAnimation.speed(partialTicks);
				this.rightfrontupperleg.xRot = -2.0F - Mth.cos(ageInTicks * 0.2F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
				this.leftfrontupperleg.xRot = -2.0F - Mth.cos(ageInTicks * 0.2F - 3.1415927F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
			}
			if (entity.getAnimationID() == 1)
				switch (entity.getAntiTitanAttackAnimationID()) {
				case 0:
					this.animateAntiTitanAttack1();
					break;
				case 1:
					this.animateAntiTitanAttack2();
					break;
				case 2:
					this.animateAntiTitanAttack3();
					break;
				case 3:
					this.animateAntiTitanAttack4();
					break;
				}
			this.animateStunned();
			this.animateThunderClap();
			this.animateSpit();
			this.animateKick();
			this.animateStomp();
			this.animateAttackStomp();
			this.animateHeadSlam();
			this.animateBirth();
		} else {
			this.animateDeath();
		}
	}

