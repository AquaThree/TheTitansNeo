	public void animate(EntitySkeletonTitan entity, float limbSwing, float limbSwingAmount, float ageInTicks, float headYaw, float headPitch) {
		float partialTicks = Minecraft.getInstance().getTimer().getGameTimeDeltaPartialTick(false);
		this.animator.update(entity);
		this.setAngles();

		float fo = 0.6662F / ((entity.getSkeletonType() == 1) ? 2.5F : 4.0F);
		if (entity.deathTicks <= 0) {
			float f6 = (float) Math.cos(ageInTicks * 0.1F);
			if (entity.getAnimationID() == 0) {
				this.spine3.xRot = (0.001F + 0.0025F * f6) * Mth.PI;
				this.spine4.xRot = (0.001F + 0.0025F * f6) * Mth.PI;
				this.spine5.xRot = (0.001F + 0.0025F * f6) * Mth.PI;
				this.spine6.xRot = (0.001F + 0.0025F * f6) * Mth.PI;
				this.head.xRot = (0.001F + 0.005F * f6) * Mth.PI;
				this.rightShoulder.yRot = (0.025F + 0.025F * f6) * Mth.PI;
				this.leftShoulder.yRot = (-0.025F + -0.025F * f6) * Mth.PI;
			}
			if (this.riding) {
				this.rightThigh.xRot = -1.5707964F;
				this.leftThigh.xRot = -1.5707964F;
				this.rightThigh.yRot = 0.31415927F;
				this.leftThigh.yRot = -0.31415927F;
				this.rightFemur.xRot = 1.0471976F;
				this.leftFemur.xRot = 1.0471976F;
			} else if (entity.getAnimationID() == 0) {
				this.hips.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				this.rightThigh.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				this.leftThigh.y = 12.0F - Mth.cos(limbSwing * fo * 2.0F + 1.0F) * 2.0F * limbSwingAmount;
				this.head.yRot = Mth.cos(limbSwing * fo + 3.25F) * 0.3F * limbSwingAmount;
				this.spine6.yRot = Mth.cos(limbSwing * fo - 3.0F) * 0.1F * limbSwingAmount;
				this.spine5.yRot = Mth.cos(limbSwing * fo - 2.5F) * 0.1F * limbSwingAmount;
				this.spine4.yRot = Mth.cos(limbSwing * fo - 2.0F) * 0.1F * limbSwingAmount;
				this.spine3.yRot = Mth.cos(limbSwing * fo - 1.5F) * 0.1F * limbSwingAmount;
				this.spine2.yRot = Mth.cos(limbSwing * fo - 1.0F) * 0.1F * limbSwingAmount;
				this.spine1.yRot = Mth.cos(limbSwing * fo - 0.5F) * 0.1F * limbSwingAmount;
				this.hips.yRot = Mth.cos(limbSwing * fo) * 0.2F * limbSwingAmount;
				this.rightThigh.xRot = -0.08726646F + Mth.cos(limbSwing * fo - 0.5F) * 0.75F * limbSwingAmount;
				this.leftThigh.xRot = -0.08726646F + Mth.cos(limbSwing * fo + 2.6415927F) * 0.75F * limbSwingAmount;
				this.rightFemur.xRot = 0.13962634F + Mth.cos(limbSwing * fo + Mth.PI) * 0.75F * limbSwingAmount;
				this.leftFemur.xRot = 0.13962634F + Mth.cos(limbSwing * fo) * 0.75F * limbSwingAmount;
				if (this.rightFemur.xRot < 0.13962634F) {
					this.rightFemur.xRot = 0.13962634F;
				}
				if (this.leftFemur.xRot < 0.13962634F) {
					this.leftFemur.xRot = 0.13962634F;
				}
				if (!this.riding) {
					this.rightShoulder.xRot = 0.08726646F + Mth.cos(limbSwing * fo + 3.1415927F) * 1.0F * limbSwingAmount;
					this.leftShoulder.xRot = 0.08726646F + Mth.cos(limbSwing * fo) * 1.0F * limbSwingAmount;
					this.rightForearm.xRot = -0.17453292F + Mth.cos(limbSwing * fo + 2.1415927F) * 1.0F * limbSwingAmount;
					this.leftForearm.xRot = -0.17453292F + Mth.cos(limbSwing * fo - 1.0F) * 1.0F * limbSwingAmount;
					if (this.rightForearm.xRot > -0.17453292F) {
						this.rightForearm.xRot = -0.17453292F;
					}
					if (this.leftForearm.xRot > -0.17453292F) {
						this.leftForearm.xRot = -0.17453292F;
					}
				}
			}
			float faceYaw = headYaw * Mth.PI / 180.0F;
			float facePitch = headPitch * Mth.PI / 180.0F;
			if (entity.getAnimationID() == 0) {
				this.head.xRot += facePitch * 0.5F;
				this.head.yRot += faceYaw * 0.5F;
				this.spine6.xRot += facePitch * 0.1F;
				this.spine6.yRot += faceYaw * 0.1F;
				this.spine5.xRot += facePitch * 0.1F;
				this.spine5.yRot += faceYaw * 0.1F;
				this.spine4.xRot += facePitch * 0.1F;
				this.spine4.yRot += faceYaw * 0.1F;
				this.spine3.xRot += facePitch * 0.1F;
				this.spine3.yRot += faceYaw * 0.1F;
				this.rightShoulder.xRot -= facePitch * 0.4F;
				this.leftShoulder.xRot -= facePitch * 0.4F;
			} else {
				this.head.xRot += facePitch * 0.9F;
				this.head.yRot += faceYaw * 0.9F;
			}
			if (!entity.onGround() && !this.riding) {
				this.spine6.yRot = 0.0F;
				this.spine5.yRot = 0.0F;
				this.spine4.yRot = 0.0F;
				this.spine3.yRot = 0.0F;
				this.spine2.zRot = 0.0F;
				this.spine1.zRot = 0.0F;
				this.hips.yRot = 0.0F;
				this.head.yRot = 0.0F;
				this.rightShoulder.xRot = -entity.walkAnimation.speed(partialTicks);
				this.leftShoulder.xRot = -entity.walkAnimation.speed(partialTicks);
				this.rightForearm.xRot = -(entity.walkAnimation.speed(partialTicks) / 2.0F);
				this.leftForearm.xRot = -(entity.walkAnimation.speed(partialTicks) / 2.0F);
				this.head.xRot -= entity.walkAnimation.speed(partialTicks);
				this.hips.xRot += entity.walkAnimation.speed(partialTicks);
				this.rightThigh.yRot = 0.25F;
				this.leftThigh.yRot = -0.25F;
				this.rightThigh.yRot = -0.25F;
				this.leftThigh.yRot = 0.25F;
				this.rightThigh.xRot = Mth.cos(ageInTicks * 0.2F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
				this.leftThigh.xRot = Mth.cos(ageInTicks * 0.2F - 3.1415927F) * 0.25F - (float) (entity.getDeltaMovement().y / 5.0D) + entity.walkAnimation.speed(partialTicks);
				this.rightFemur.xRot = 0.5F - Mth.cos(ageInTicks * 0.2F + 0.5F) * 0.5F;
				this.leftFemur.xRot = 0.5F - Mth.cos(ageInTicks * 0.2F - 2.6415927F) * 0.5F;
			}
			if (entity.getAnimationID() == 5 && entity.getAnimationTick() > 100 && entity.getAnimationTick() < 300) {
				this.leftForearm.xRot = -Mth.cos(ageInTicks * 4.0F) * 0.5F;
			}
			if (entity.getAnimationID() == 1)
				switch (entity.getAntiTitanAttackAnimationID()) {
				case 0:
					this.animateAntiTitanAttack1();
					break;
				case 1:
					this.animateAntiTitanAttack2();
					break;
				case 2:
					this.animateAntiTitanAttack3();
					break;
				case 3:
					this.animateAntiTitanAttack4();
					break;
				}
			this.animatePunch();
			this.animateBackhand();
			this.animateSwat();
			this.animateShot();
			this.animateStomp();
			this.animateDownwardSlash();
			this.animateStun();
			this.animateLightning();
			this.animateGiantShot();
			this.animateLightningHand();
			this.animateBirth();
		} else {
			this.animateDeath();
		}
	}

